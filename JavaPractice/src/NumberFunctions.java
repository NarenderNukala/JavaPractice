
public class NumberFunctions {
	
	public static void main(String args[]) {
		
		System.out.println("Absolute Method on -30 = " + Math.abs(-30) );
		
		System.out.println("Ceil Method on 20.45 = " + Math.ceil(20.45) );
		
		System.out.println("Floor Method on 20.45 = " + Math.floor(20.45) );
		
		System.out.println("Round Method on 20.45 = " + Math.round(20.45) );
		System.out.println("Round Method on 20.55 = " + Math.round(20.55) );
		
		System.out.println("Min of 23, 42 = " + Math.min(23,42) );
		System.out.println("Max of 23, 42 = " + Math.max(23,42) );
		
		System.out.println("2 Power 3  = " + Math.pow(2,3) );
		
		System.out.println("Sequre root of 9  = " + Math.sqrt(9) );
		System.out.println("Sequre root of 8  = " + Math.sqrt(8) );
		
		System.out.println("sin(90) = " + Math.sin(90) );
		System.out.println("cos(90) = " + Math.tan(90) );
		
		System.out.println("Random Value between 0.0 and 1.0 = " + Math.random() );
		System.out.println("Random Value between 0.0 and 1.0 = " + Math.random() );
		System.out.println("Random Value between 0.0 and 1.0 = " + Math.random() * 100 );
		System.out.println("Random Value between 0.0 and 1.0 = " + Math.random() * 100 );
		
	}

}
