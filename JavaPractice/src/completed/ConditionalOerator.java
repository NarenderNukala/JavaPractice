package completed;

public class ConditionalOerator {

	public static void main(String args[]) {
		int a,b;
		a=10;
		b=20;
		System.out.println("Biggest of two numbers is " + ((a>b)?a:b));
		
		int c,d;
		c = 20;
		d = -30;
		System.out.println("Absolute Number " + ((c>0)?c:-c));
		System.out.println("Absolute Number " + ((d>0)?d:-d));
		
	}
}
